package oops;

public class EncapsulationExample {

	private int balance;
	 private String Name;
	 
	 
	public int getBalance() {
		return balance;
	}
	public void setBalance(int balance) {
		this.balance = balance;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	 
}
